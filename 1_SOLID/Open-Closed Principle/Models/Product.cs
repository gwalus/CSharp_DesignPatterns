namespace Open_Closed_Principle.Models
{
    public enum Color
    {
        Green, Yellow, Blue, Red, Pink
    }

    public enum Size
    {
        Small, Medium, Large, Yuge
    }

    public class Product
    {
        public string Name;
        public Color Color;
        public Size Size;

        public Product(string name, Color color, Size size)
        {
            Name = name;
            Color = color;
            Size = size;
        }
    }
}
